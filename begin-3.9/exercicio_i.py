'''i) Ler dois inteiros (variáveis A e B) e imprimir o resultado do quadrado da diferença do primeiro valor pelo 
segundo. '''

a = int(input("Digite o primeiro valor: "))
b = int(input("Digite o segundo valor: "))

diferenca = a - b

quad_diferenca = diferenca * diferenca

print(f"O resultado do quadrado da diferença é {quad_diferenca}")